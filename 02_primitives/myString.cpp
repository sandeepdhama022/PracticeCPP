#include <iostream>
#include <string>

using namespace std;

int main() {
    string favoriteCity = "Dubai";
    string myDream = "To Settle in Dubai";

    cout << "My favorite city is " << favoriteCity << endl;
    cout << "My \"dream\" is " << myDream << endl; // Using escape character we can include quotes in the string
    cout << "I am checking \n new line" << endl;
    return 0;
}